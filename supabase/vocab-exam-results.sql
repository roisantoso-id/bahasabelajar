-- BIPA 模拟测评结果表
-- 在 Supabase SQL Editor 运行：
-- 1) 先执行本文件创建表
-- 2) 前端通过 vocab-test Edge Function 写入，页面自己读取最新记录做对比

create table if not exists public.vocab_exam_results (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid() references auth.users(id) on delete cascade,
  estimate integer not null,
  level_code text not null,
  level_name text not null,
  word_correct integer not null,
  word_total integer not null,
  sentence_correct integer not null,
  sentence_total integer not null,
  reading_correct integer not null,
  reading_total integer not null,
  overall_rate numeric(5,4) not null,
  stage_difficulty jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create index if not exists idx_vocab_exam_results_user_created_at
  on public.vocab_exam_results (user_id, created_at desc);

alter table public.vocab_exam_results enable row level security;

drop policy if exists "vocab_exam_results_read_own" on public.vocab_exam_results;
drop policy if exists "vocab_exam_results_insert_own" on public.vocab_exam_results;
create policy "vocab_exam_results_read_own"
  on public.vocab_exam_results
  for select
  using (auth.uid() = user_id);
create policy "vocab_exam_results_insert_own"
  on public.vocab_exam_results
  for insert
  with check (auth.uid() = user_id);

grant select, insert on table public.vocab_exam_results to authenticated;
grant usage, select on sequence public.vocab_exam_results_id_seq to authenticated;
