// 词性兜底（按 pos）
const POS_FALLBACK = {
  'pron.': '人称指代',
  'num.': '数字时间',
  'conj.': '虚词语法',
  'prep.': '虚词语法',
  'interj.': '问候寒暄',
  'v.': '动作行为',
  'adj.': '性质状态',
  'adv.': '性质状态',
}

function classify(word, meaning, pos) {
  const w = String(word || '').trim().toLowerCase()
  if (WORD_OVERRIDE[w]) return WORD_OVERRIDE[w]
  const m = String(meaning || '')
  // 1) 代词/数词/连词/介词：词性信号最强
  if (pos === 'pron.') return '人称指代'
  if (pos === 'num.') return '数字时间'
  if (pos === 'conj.' || pos === 'prep.') return '虚词语法'
  // 2) 关键词命中（按优先级顺序）
  for (const [topic, kws] of RULES) {
    for (const kw of kws) {
      const k = kw.replace(/（[^）]*）/g, '')
      if (k && m.includes(k)) return topic
    }
  }
  // 3) 词性兜底
  if (POS_FALLBACK[pos]) return POS_FALLBACK[pos]
  return '其他'
}

